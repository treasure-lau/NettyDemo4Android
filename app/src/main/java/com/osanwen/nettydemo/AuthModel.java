public class AuthModel {
    private int i;
    private String u;
    private String n;
    private String f;
    private int t;

    public int getI() {
        return i;
    }

    public void setI(int i) {
        this.i = i;
    }

    public String getU() {
        return u;
    }

    public void setU(String u) {
        this.u = u;
    }

    public String getN() {
        return n;
    }

    public void setN(String n) {
        this.n = n;
    }

    public String getF() {
        return f;
    }

    public void setF(String f) {
        this.f = f;
    }

    public int getT() {
        return t;
    }

    public void setT(int t) {
        this.t = t;
    }
}
